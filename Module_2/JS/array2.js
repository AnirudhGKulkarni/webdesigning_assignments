//strings array
let fruits=['apple','banana','orange','grape'];
//print the array
console.log("Initial array is:",fruits);
//push operation to insert an element
fruits.push("guava"); //gets added in the last
//print after push
console.log("After push operation:",fruits);
//pop  operation to delete an element
fruits.pop(1);//last element gets deleted
//print after pop
console.log("After pop operation:",fruits);
