//Creating an object using object literal notation
const person= {
    firstName: "Ani",
    lastName: "Kulkarni",
    age: 19,
    occupation: "Student",
};
//Accessing object properties using dot notation
console.log(person.firstName); //Output: Ani
console.log(person.lastName); //Output: Kulkarni
console.log(person.age);      //Output: 19
console.log(person.occupation);//Output: Student